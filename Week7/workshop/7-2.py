"""
โจทย์ที่ 2: ฟังก์ชันคำนวณพื้นที่วงกลม
สร้างฟังก์ชันชื่อ print_circle_area ที่:
1. รับพารามิเตอร์ radius (รัศมี)
2. คำนวณพื้นที่วงกลม (π × r²)
3. แสดงผลในรูปแบบ "วงกลมรัศมี [radius] หน่วย มีพื้นที่ [area] ตารางหน่วย"
4. ใช้ค่า π = 3.14159
5. ทดสอบด้วยรัศมี 5, 7.5, และ 10
"""

def print_circle_area(radius):
    """ฟังก์ชันคำนวณและแสดงพื้นที่วงกลม"""
    pi = 3.14159
    area = pi * radius * radius
    print(f"วงกลมรัศมี {radius} หน่วย มีพื้นที่ {area:.2f} ตารางหน่วย")

# ทดสอบฟังก์ชัน
print("ผลการทดสอบ:")
print_circle_area(5)
print_circle_area(7.5)
print_circle_area(10)