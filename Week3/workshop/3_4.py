# รับอายุ
age = int(input("กรุณาใส่อายุของคุณ: "))

# ใช้ if-elif-else เพื่อกำหนดราคาตามเงื่อนไข
if age <= 12:
    price = 80
elif age <= 59: # ไม่ต้องเช็คว่า >= 13 เพราะถ้าอายุน้อยกว่า 12 จะเข้า if แรกไปแล้ว
    price = 150
else: # กรณีอื่นๆ ทั้งหมด (คือ 60 ปีขึ้นไป)
    price = 100

print(f"ราคาตั๋วของคุณคือ {price} บาท")