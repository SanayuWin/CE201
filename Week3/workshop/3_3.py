# รับคะแนน
score = int(input("ป้อนคะแนนของคุณ: "))

# กำหนดเกรดเริ่มต้นเป็น 'F'
# ถ้าคะแนนไม่เข้าเงื่อนไข if ใดๆ เลย ค่านี้จะเป็นคำตอบสุดท้าย
grade = 'F'

# โปรแกรมจะตรวจสอบทุก if อย่างต่อเนื่อง
# ถ้าเงื่อนไขเป็นจริง ค่า grade จะถูก "เขียนทับ" ไปเรื่อยๆ
# เช่น ถ้าได้ 85, grade จะเปลี่ยนจาก F -> D -> C -> B -> A
if score >= 50:
    grade = 'D'

if score >= 60:
    grade = 'C'

if score >= 70:
    grade = 'B'
    
if score >= 80:
    grade = 'A'

# แสดงผลค่า grade สุดท้ายที่ถูกกำหนด
print(f"เกรดของคุณคือ: {grade}")
