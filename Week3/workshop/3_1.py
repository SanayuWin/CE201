# รับค่าปี ค.ศ.
year = int(input("ป้อนปี ค.ศ. ที่ต้องการตรวจสอบ: "))

# เงื่อนไขที่ 1: หารด้วย 4 ลงตัว และ หารด้วย 100 ไม่ลงตัว
condition1 = (year % 4 == 0) and (year % 100 != 0)

# เงื่อนไขที่ 2: หารด้วย 400 ลงตัว
condition2 = (year % 400 == 0)

# นำเงื่อนไขทั้งสองมารวมกันด้วย or
# ถ้าเงื่อนไขใดเงื่อนไขหนึ่งเป็นจริง ก็ถือว่าเป็นปีอธิกสุรทิน
if condition1 or condition2:
    print(f"ปี {year} เป็นปีอธิกสุรทิน (Leap Year)")
else:
    print(f"ปี {year} ไม่เป็นปีอธิกสุรทิน (Not a Leap Year)")