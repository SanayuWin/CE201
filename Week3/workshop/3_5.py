# รับค่าจากผู้เล่นทั้งสอง
p1 = input("ผู้เล่น 1 (ค้อน, กรรไกร, กระดาษ): ")
p2 = input("ผู้เล่น 2 (ค้อน, กรรไกร, กระดาษ): ")    

# 1. เช็คกรณีก่อนเสมอ
if p1 == p2:
    print("เสมอ!")
# 2. เช็คทุกกรณีที่ผู้เล่น 1 จะชนะ
elif (p1 == 'ค้อน' and p2 == 'กรรไกร') or \
     (p1 == 'กรรไกร' and p2 == 'กระดาษ') or \
     (p1 == 'กระดาษ' and p2 == 'ค้อน'):
    print("ผู้เล่น 1 ชนะ!")
# 3. ถ้าไม่เสมอ และผู้เล่น 1 ไม่ชนะ ก็แสดงว่าผู้เล่น 2 ชนะ
else:
    print("ผู้เล่น 2 ชนะ!")

