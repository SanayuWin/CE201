x = 15
y = 4

print(f"{x} + {y} = {x + y}")       # ผลลัพธ์: 15 + 4 = 19
print(f"{x} / {y} = {x / y}")       # ผลลัพธ์: 15 / 4 = 3.75
print(f"{x} // {y} = {x // y}")     # ผลลัพธ์: 15 // 4 = 3 (เอาแค่ 3)
print(f"{x} % {y} = {x % y}")       # ผลลัพธ์: 15 % 4 = 3 (15 หาร 4 ได้ 3 เศษ 3)
print(f"5 ** 3 = {5 ** 3}")         # ผลลัพธ์: 5 ** 3 = 125

