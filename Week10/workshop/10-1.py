# ร้านกาแฟแห่งหนึ่งมีเมนูเริ่มต้นดังนี้ ให้เขียนโปรแกรมเพื่อทำงานตามลำดับต่อไปนี้:
# - แสดงราคาของเมนู "Latte"
# - เพิ่มเมนูใหม่ "Espresso" ราคา 50 บาท
# - ลบเมนู "Mocha" ออกจากรายการ
# - แสดงเมนูล่าสุดทั้งหมด (ทั้งชื่อและราคา)

# โค้ดเริ่มต้น
menu = {
    "Americano": 55,
    "Latte": 60,
    "Mocha": 65
}

# 1. แสดงราคาของเมนู "Latte"
print(f"ราคาลาเต้: {menu['Latte']} บาท")

# 2. เพิ่มเมนูใหม่ "Espresso" ราคา 50 บาท
menu['Espresso'] = 50
print("เพิ่มเมนู Espresso เรียบร้อยแล้ว")

# 3. ลบเมนู "Mocha" ออกจากรายการ
del menu['Mocha']
print("ลบเมนู Mocha เรียบร้อยแล้ว")

# 4. แสดงเมนูล่าสุดทั้งหมด
print("\n--- เมนูปัจจุบัน ---")
for name, price in menu.items():
    print(f"- {name}: {price} บาท")