# เพื่อนสองคนไปซื้อของมา และนี่คือรายการของที่แต่ละคนซื้อ 
# จงเขียนโปรแกรมเพื่อหา "รายการของทั้งหมดที่ไม่ซ้ำกัน" ที่ทั้งสองคนซื้อรวมกัน

# โค้ดเริ่มต้น
shopping_list_A = ['ไข่', 'นม', 'ขนมปัง', 'นม', 'น้ำส้ม']
shopping_list_B = ['กาแฟ', 'นม', 'น้ำตาล', 'ไข่', 'ไข่']

# 1. แปลง List ให้เป็น Set เพื่อลบรายการที่ซ้ำกันภายใน List ของแต่ละคน
items_A = set(shopping_list_A)
items_B = set(shopping_list_B)

# 2. ใช้ Union เพื่อรวมของจากทั้งสอง Set เข้าด้วยกัน (ตัวซ้ำระหว่าง Set จะถูกรวมเป็นตัวเดียว)
all_unique_items = items_A.union(items_B)
# หรือสามารถใช้ตัวย่อ: all_unique_items = items_A | items_B

# 3. แสดงผลลัพธ์
print("รายการของทั้งหมด (ไม่ซ้ำกัน):")
print(all_unique_items)