# สร้างตาราง 3x4 (3 แถว, 4 คอลัมน์)
# เหมือนตารางที่มีข้อมูลนักเรียน 3 คน แต่ละคนมี 4 วิชา
student_scores = [
    [80, 85, 88, 90],  # แถว 0: คะแนนของนักเรียนคนที่ 1
    [92, 89, 94, 88],  # แถว 1: คะแนนของนักเรียนคนที่ 2
    [78, 82, 80, 84]   # แถว 2: คะแนนของนักเรียนคนที่ 3
]

print("คะแนนของนักเรียนแต่ละคน:" + str(student_scores))


# ดึงคะแนนวิชาที่ 3 (column 2) ของนักเรียนคนที่ 2 (row 1)
score = student_scores[1][2]
print(f"คะแนนที่ต้องการคือ: {score}") # ผลลัพธ์: 94

# ดึงคะแนนวิชาแรก (column 0) ของนักเรียนคนแรก (row 0)
first_score = student_scores[0][0]
print(f"คะแนนวิชาแรกของคนแรก: {first_score}") # ผลลัพธ์: 80

# แก้ไขคะแนนวิชาสุดท้าย (column 3) ของนักเรียนคนที่ 3 (row 2)
print(f"คะแนนเดิม: {student_scores[2][3]}") # 84
student_scores[2][3] = 87
print(f"คะแนนใหม่: {student_scores[2][3]}") # 87

