import numpy as np

# 1. สร้างตารางขนาด 8x8 ที่มีแต่เลข 0 ทั้งหมด
chessboard = np.zeros((8, 8), dtype=int)

# 2. เติมเลข 1 ในช่องที่เป็นสีดำ
# เติมแถวคู่ (0, 2, 4, 6) ในคอลัมน์คู่ (0, 2, 4, 6)
chessboard[::2, ::2] = 1

# เติมแถวคี่ (1, 3, 5, 7) ในคอลัมน์คี่ (1, 3, 5, 7)
chessboard[1::2, 1::2] = 1

print(chessboard)