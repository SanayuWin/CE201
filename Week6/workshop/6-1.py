matrix = [
    [10, 20, 30],
    [40, 50, 60],
    [70, 80, 90]
]

# 1. สร้างตัวแปรสำหรับเก็บผลรวม เริ่มต้นที่ 0
total_sum = 0

# 2. ใช้ลูปนอกเพื่อวนแต่ละแถว
for row in matrix:
    # 3. ใช้ลูปในเพื่อวนแต่ละตัวเลขในแถวนั้นๆ
    for number in row:
        # 4. นำตัวเลขมาบวกสะสมใน total_sum
        total_sum += number

print(f"ผลรวมของตัวเลขทั้งหมด (วิธีใช้ List): {total_sum}")
