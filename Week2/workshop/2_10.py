# 1. สร้างตัวแปรชั่วคราว (temp) เพื่อเก็บค่าของ a ไว้ก่อน
temp = a
# 2. ตอนนี้ a ว่างแล้ว, เอาค่าจาก b มาใส่ใน a
a = b
# 3. เอาค่าดั้งเดิมของ a ที่เก็บใน temp กลับมาใส่ใน b
b = temp

print("--- กำลังสลับค่า... ---")
# แสดงผลลัพธ์หลังสลับค่า
