# รับค่าน้ำหนักและส่วนสูง
weight_kg = float(input("ป้อนน้ำหนักของคุณ (กิโลกรัม): "))
height_m = float(input("ป้อนส่วนสูงของคุณ (เมตร): "))

# คำนวณ BMI (ใช้ ** 2 สำหรับยกกำลังสอง)
bmi = weight_kg / (height_m ** 2)

# แสดงผล (ใช้ :.2f ใน f-string เพื่อจัดรูปแบบทศนิยม 2 ตำแหน่ง)
print(f"ค่าดัชนีมวลกาย (BMI) ของคุณคือ: {bmi:.2f}")