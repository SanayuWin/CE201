# ---------- ส่วนของ Output (ต้อนรับผู้ใช้) ----------
print("--- โปรแกรมคำนวณพื้นที่สี่เหลี่ยม ---")
print("กรุณาป้อนความกว้างและความสูงเป็นตัวเลข")

# ---------- ส่วนของ Input (รับข้อมูล) ----------
# รับค่าความกว้าง (เป็น str)
width_str = input("ป้อนความกว้าง: ")

# รับค่าความสูง (เป็น str)
height_str = input("ป้อนความสูง: ")

# ---------- ส่วนของ Process (ประมวลผล) ----------
# *** ขั้นตอนสำคัญ: แปลง str เป็นตัวเลข (float) เพื่อนำไปคำนวณ ***
width_float = float(width_str)
height_float = float(height_str)

# คำนวณหาพื้นที่
area = width_float * height_float

# ---------- ส่วนของ Output (แสดงผลลัพธ์) ----------
print("--- ผลการคำนวณ ---")
print(f"สี่เหลี่ยมที่มีความกว้าง {width_float} หน่วย และความสูง {height_float} หน่วย")
print(f"จะมีพื้นที่เท่ากับ {area} ตารางหน่วย")


