# รับความสูงของสามเหลี่ยม
height = int(input("ป้อนความสูงของสามเหลี่ยม: "))

# ใช้ for loop วนตั้งแต่ 1 จนถึงความสูงที่กำหนด
# range(1, height + 1) เพื่อให้ได้ตัวเลข 1, 2, 3, ..., height
for i in range(1, height + 1):
    # ใน Python เราสามารถใช้เครื่องหมาย * กับ String เพื่อทำซ้ำได้
    # รอบที่ i = 1, จะได้ "*" * 1 -> "*"
    # รอบที่ i = 2, จะได้ "*" * 2 -> "**"
    # ...
    print("*" * i)