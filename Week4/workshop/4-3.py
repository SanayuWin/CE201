correct_pin = "1234"
attempts_left = 3

# ลูปจะทำงานตราบใดที่ยังมีจำนวนครั้งเหลืออยู่
while attempts_left > 0:
    print(f"คุณมีโอกาสกดรหัสได้อีก {attempts_left} ครั้ง")
    entered_pin = input("กรุณาใส่รหัส 4 หลัก: ")

    if entered_pin == correct_pin:
        print("รหัสถูกต้อง ทำรายการสำเร็จ!")
        break # ออกจากลูปเมื่อทำรายการสำเร็จ
    else:
        print("รหัสไม่ถูกต้อง!")
        attempts_left -= 1 # ลดจำนวนครั้งที่เหลือลง

# โค้ดส่วนนี้จะทำงานก็ต่อเมื่อออกจาก while loop แล้ว
# เราต้องเช็คว่าที่ออกมาเพราะทำสำเร็จ หรือเพราะสิทธิ์หมด
if attempts_left == 0:
    print("คุณกดรหัสผิดครบ 3 ครั้ง บัตรของคุณถูกระงับ")